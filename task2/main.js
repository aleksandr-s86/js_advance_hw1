/*Вы управляете рестораном, в котором работают разные повара, специализирующиеся на определенных блюдах. Клиенты приходят и делают заказы на разные блюда.

Необходимо создать систему управления этими заказами, которая позволит:

• Отслеживать, какой повар готовит какое блюдо.
• Записывать, какие блюда заказал каждый клиент.

Используйте коллекции Map для хранения блюд и их поваров, а также для хранения заказов каждого клиента. В качестве ключей для клиентов используйте объекты.

Повара и их специализации:

Виктор - специализация: Пицца.
Ольга - специализация: Суши.
Дмитрий - специализация: Десерты.

Блюда и их повара:

Пицца "Маргарита" - повар: Виктор.
Пицца "Пепперони" - повар: Виктор.
Суши "Филадельфия" - повар: Ольга.
Суши "Калифорния" - повар: Ольга.
Тирамису - повар: Дмитрий.
Чизкейк - повар: Дмитрий.

Заказы:

Клиент Алексей заказал: Пиццу "Пепперони" и Тирамису.
Клиент Мария заказала: Суши "Калифорния" и Пиццу "Маргарита".
Клиент Ирина заказала: Чизкейк.*/

// Создаем объект для хранения информации о блюдах и их поварах
const dishChefMap = new Map();
dishChefMap.set("Маргарита", "Виктор");
dishChefMap.set("Пепперони", "Виктор");
dishChefMap.set("Филадельфия", "Ольга");
dishChefMap.set("Калифорния", "Ольга");
dishChefMap.set("Тирамису", "Дмитрий");
dishChefMap.set("Чизкейк", "Дмитрий");

// Создаем объект для хранения заказов клиентов
const customerOrdersMap = new Map();

// Заполняем заказы клиентов
customerOrdersMap.set("Алексей", new Map([
    ["Пепперони", 1],
    ["Тирамису", 1]
]));

customerOrdersMap.set("Мария", new Map([
    ["Калифорния", 1],
    ["Маргарита", 1]
]));

customerOrdersMap.set("Ирина", new Map([
    ["Чизкейк", 1]
]));

// Функция для вывода информации о блюдах и их поварах
function printDishChefMap() {
    console.log("Блюда и их повара:");
    dishChefMap.forEach((chef, dish) => {
        console.log(`Блюдо: ${dish}, Повар: ${chef}`);
    });
}

// Функция для вывода информации о заказах клиентов
function printCustomerOrdersMap() {
    console.log("\nЗаказы клиентов:");
    customerOrdersMap.forEach((orders, customer) => {
        console.log(`Клиент: ${customer}`);
        orders.forEach((quantity, dish) => {
            console.log(`  Блюдо: ${dish}, Количество: ${quantity}`);
        });
    });
}

// Пример вызова функций для вывода данных
printDishChefMap();
printCustomerOrdersMap();